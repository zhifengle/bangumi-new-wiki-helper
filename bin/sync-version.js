const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const packagePath = path.join(rootDir, 'package.json');
const manifestPath = path.join(rootDir, 'extension', 'manifest.json');
const versionTsPath = path.join(rootDir, 'src', 'version.ts');

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, 'utf8'));
}

function writeJson(filePath, data) {
  fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
}

function syncManifestVersion(version) {
  const manifest = readJson(manifestPath);
  if (manifest.version !== version) {
    manifest.version = version;
    writeJson(manifestPath, manifest);
  }
}

function syncVersionModule(version) {
  const content =
    `// This file is generated by bin/sync-version.js.\n` +
    `export const APP_VERSION = '${version}';\n`;
  fs.writeFileSync(versionTsPath, content, 'utf8');
}

function main() {
  const pkg = readJson(packagePath);
  const version = pkg.version;
  if (!version) {
    throw new Error('package.json version is missing');
  }
  syncManifestVersion(version);
  syncVersionModule(version);
  console.log(`Synced project version: ${version}`);
}

main();
